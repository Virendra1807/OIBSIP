package OnlineExamination;

public class StudInformation {
    
    private int LoginId;
    private int password;

    public StudInformation(){

    }

    public int getLoginId(){
        return LoginId;
    }
    
    public void setLoginId(int a){
        LoginId = a;
    }

    public int getPassword(){
        return password;
    }
    
    public void setPassword(int b){
        password = b;
    }
}
